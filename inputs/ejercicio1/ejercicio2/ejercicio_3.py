#creando una funcion que muestre la serie fibonachi entre el 0 y el numero dado 

def fibonachi (num):
    a,b =0,1 
    fibonachi_lista=[0]
    for i in range(num):
        if b > num:return fibonachi_lista
        else :
            fibonachi_lista.append(b)
            a,b = b,a+b 
    

resultado = fibonachi(32)
print(resultado)
